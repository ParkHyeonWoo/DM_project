package com.board.domain;

public class RuneInfoDTO {
	private String rune_id;
	private String rune_name;
	private String rune_name_kr;
	private String rune_desc;
	
	public String getRune_id() {
		return rune_id;
	}
	public void setRune_id(String rune_id) {
		this.rune_id = rune_id;
	}
	public String getRune_name() {
		return rune_name;
	}
	public void setRune_name(String rune_name) {
		this.rune_name = rune_name;
	}
	public String getRune_name_kr() {
		return rune_name_kr;
	}
	public void setRune_name_kr(String rune_name_kr) {
		this.rune_name_kr = rune_name_kr;
	}
	public String getRune_desc() {
		return rune_desc;
	}
	public void setRune_desc(String rune_desc) {
		this.rune_desc = rune_desc;
	}

}
